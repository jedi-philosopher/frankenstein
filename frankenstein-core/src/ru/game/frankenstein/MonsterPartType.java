package ru.game.frankenstein;

/**
 * Enum for monster part types.
 */
public enum MonsterPartType
{
    /**
     * Main part of a monster
     */
    MONSTER_BODY,
    /**
     * External limbs like tentacles, legs, wings etc
     */
    MONSTER_LIMB,
    /**
     * Some additional parts, that have no attachment points and are used for decoration. Like eyes, mouths.
     */
    MONSTER_DECORATION
}
